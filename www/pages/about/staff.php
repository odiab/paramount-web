<h1>Meet Our Doctors</h1>
<p> The professional members of our staff have 15 to twenty years of
    experience in the different phases of clinical trials, mastering
    excellence in both industry- and publicly-sponsored programs.
</p>

<h3>Principal Investigator: Dr. Isam A. Diab, M.D.</h3>
<p> Dr. Isam A. Diab specializes in rheumatology and  has been conducting
    clinical research for nearly 20 years as both a Principal-Investigator
    and Sub-Investigator. Maintaining collaboration with approximately 75
    private practitioners in a large multi-disciplinary network, Dr. Diab
    has grown his private practice into one of the largest within the
    United States. His thorough understanding of medicine along with
    enthusiasm for patient needs is clearly seen throughout his oversight
    of the clinical research process.
</p>

<h3>Sub-Investigator: Dr. Mirza I. Baig, M.D.</h3>
<p>Dr. Mirza I. Baig supports the clinical research  process in his role as Sub-Investigator. He maintains a vibrant practice of his  own in the area of physical medicine.</p>

