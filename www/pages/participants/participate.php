<h1>Potential Participants</h1>
<p> Clinical research participants can volunteer to participate in a trial
    or be invited by the research team to consider the  option of getting
    involved in a research trial. Participants have largely  benefitted
    from the one-on-one attention. Many choose to participate in order to
    gain more information about his/her particular condition. Some studies
    have shown that those who choose to participate in a study do better
    than their counterparts, despite the treatment arm of the protocol.
    Still others find that contributing to the research process gives them
    a sense of helping others, as well as future generations.
</p>
